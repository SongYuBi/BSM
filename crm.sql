  CREATE OR REPLACE PROCEDURE "SHOPPING"."OLD_LEVEL" 
as
begin
insert into old_level_tb(user_id,level_id,price,reg_date)
select
t10.user_id,
t10.level_id,
NVL(t30.price,0) as price,
sysdate
from
user_tb t10,
level_tb t20,
(select t100.user_id, sum(t100.order_product_cnt * t200.product_price) as price from order_tb t100, product_tb t200 where t100.product_id = t200.product_id group by t100.user_id) t30
where t10.user_id = t30.user_id(+)
and t10.level_id = t20.level_id
and t20.level_price >= NVL(t30.price,0);
end;

